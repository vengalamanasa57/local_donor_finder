                style: Theme.of(context)
                    .textTheme
                    .bodySmall
                    ?.copyWith(color: Colors.white70),
              ),
            ),
          ),
        ),
      ),
      body: SingleChildScrollView(
        padding: const EdgeInsets.all(20),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            _WelcomeCard(),
            const SizedBox(height: 20),

            // SOS Button — opens the ModalBottomSheet
            _EmergencyButton(),
            const SizedBox(height: 28),

            // Live feed from Firestore
            Text('Recent SOS requests', style: _sectionLabelStyle()),
            const SizedBox(height: 10),

            // StreamBuilder listens to `emergencies` in real time.
            // Every time a new request is added, the UI updates automatically.
            StreamBuilder<QuerySnapshot>(
              stream: FirebaseFirestore.instance
                  .collection('emergencies')
                  .where('status', isEqualTo: 'active')
                  .orderBy('timestamp', descending: true)
                  .limit(10)
                  .snapshots(),
              builder: (context, snapshot) {
                if (snapshot.connectionState == ConnectionState.waiting) {
                  return const Center(
                    child: Padding(
                      padding: EdgeInsets.all(24),
                      child: CircularProgressIndicator(
                          color: Color(0xFFC62828)),
                    ),
                  );
                }

                if (snapshot.hasError) {
                  return Center(
                    child: Text(
                      'Error: ${snapshot.error}',
                      style: const TextStyle(color: Colors.red),
                    ),
                  );
                }

                final docs = snapshot.data?.docs ?? [];
                if (docs.isEmpty) return const _EmptyRequestsCard();

                return Column(
                  children: docs.map((doc) {
                    final request = SOSRequest.fromMap(
                      doc.id,
                      doc.data() as Map<String, dynamic>,
                    );
                    return _SOSRequestCard(request: request);
                  }).toList(),
                );
              },
            ),
          ],
        ),
      ),
    );
  }
}

