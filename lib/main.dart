// DATA MODEL — SOSRequest
// Represents a single emergency request stored in Firestore.
// Collection path: `emergencies/{id}`
// ============================================================
class SOSRequest {
  final String id;           // Firestore document ID (set after write)
  final String name;         // Requester's display name
  final String bloodType;    // e.g. "A+", "O-"
  final GeoPoint location;   // lat/lng stored as Firestore GeoPoint
  final Timestamp timestamp; // Server-side creation time

  const SOSRequest({
    required this.id,
    required this.name,
    required this.bloodType,
    required this.location,
    required this.timestamp,
  });

  // ── Firestore → Dart ──────────────────────────────────────
  // Called when reading a document snapshot from Firestore.
  factory SOSRequest.fromMap(String id, Map<String, dynamic> map) {
    return SOSRequest(
      id: id,
      name: map['name'] as String? ?? '',
      bloodType: map['bloodType'] as String? ?? 'A+',
      location: map['location'] as GeoPoint? ?? const GeoPoint(0, 0),
      timestamp: map['timestamp'] as Timestamp? ?? Timestamp.now(),
    );
  }

  // ── Dart → Firestore ──────────────────────────────────────
  // Called when writing a new document to Firestore.
  // Note: `id` is NOT included — Firestore manages the doc ID separately.
  Map<String, dynamic> toMap() {
    return {
      'name': name,
      'bloodType': bloodType,
      'location': location,
      'timestamp': timestamp,
    };
  }
}

// ============================================================
// FIRESTORE SERVICE — submitSOSRequest()
// Writes a new SOSRequest to the `emergencies` collection.
// Returns the new Firestore document ID on success.
// ============================================================
Future<String> submitSOSRequest({
  required String name,
  required String bloodType,
  required GeoPoint location,
}) async {
  final collection = FirebaseFirestore.instance.collection('emergencies');

  // `FieldValue.serverTimestamp()` lets Firestore set the timestamp
  // on the server, avoiding device clock skew across users.
  final docRef = await collection.add({
    'name': name,
    'bloodType': bloodType,
    'location': location,
    'timestamp': FieldValue.serverTimestamp(),
    'status': 'active', // useful for later — query only active requests
  });

  return docRef.id;
}

