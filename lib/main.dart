// DATA MODEL — SOSRequest
// ============================================================
class SOSRequest {
  final String id;
  final String name;
  final String bloodType;
  final GeoPoint location;
  final Timestamp timestamp;
  final String address;

  const SOSRequest({
    required this.id,
    required this.name,
    required this.bloodType,
    required this.location,
    required this.timestamp,
    this.address = "Locating...",
  });

  factory SOSRequest.fromMap(String id, Map<String, dynamic> map) {
    return SOSRequest(
      id: id,
      name: map['patientName'] as String? ?? map['name'] as String? ?? 'Urgent Request',
      bloodType: map['bloodType'] as String? ?? 'A+',
      location: map['location'] as GeoPoint? ?? const GeoPoint(0, 0),
      timestamp: map['timestamp'] as Timestamp? ?? Timestamp.now(),
      address: map['address'] as String? ?? "Location pending...",
    );
  }

  Map<String, dynamic> toMap() {
    return {
      'name': name,
      'bloodType': bloodType,
      'location': location,
      'timestamp': timestamp,
    };
  }
}

// ============================================================
// FIRESTORE SERVICE
// ============================================================
Future<String> submitSOSRequest({
  required String name,
  required String bloodType,
  required GeoPoint location,
  required String address,
  String phoneNumber = "9876543210",
}) async {
  final docRef = await FirebaseFirestore.instance
      .collection('emergencies')
      .add({
    'patientName': name,
    'bloodType': bloodType,
    'location': location,
    'address': address,
    'phoneNumber': phoneNumber,
    'timestamp': FieldValue.serverTimestamp(),
    'status': 'active',
  });
  return docRef.id;
}

