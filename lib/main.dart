                        const SnackBar(content: Text("Location not found.")),
                      );
                      return;
                    }

                    // 4. MODIFIED: Saving the REAL name and phone from controllers
                    await FirebaseFirestore.instance.collection('emergencies').add({
                      'patientName': nameController.text.trim(), // Replaces "Arjun Kumar"
                      'bloodType': selectedBloodType,
                      'location': location,
                      'timestamp': FieldValue.serverTimestamp(),
                      'address': hospitalController.text,
                      'status': 'active',
                      'hospitalName': hospitalController.text.trim(),
                      'units': unitsController.text.trim(),
                      'phone': phoneController.text.trim(), // Added phone
                    }).timeout(const Duration(seconds: 10));

                    if (!mounted) return;

                    ScaffoldMessenger.of(context).showSnackBar(
                      const SnackBar(content: Text("Request Posted Successfully!")),
                    );

                    // 5. CLEAR FIELDS: Now includes name and phone
                    nameController.clear();
                    hospitalController.clear();
                    unitsController.clear();
                    phoneController.clear();

                  } catch (e) {
                    debugPrint("CRITICAL ERROR: $e");
                    if (!mounted) return;
                    ScaffoldMessenger.of(context).showSnackBar(
                      SnackBar(content: Text("Error: ${e.toString()}")),
                    );
                  }
                },
                child: const Text('Post Request', style: TextStyle(color: Colors.white, fontSize: 16)),
              ),
            ),
          ],
        ),
      ),
    );
  }
}

